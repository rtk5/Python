#slicing on tuple

fruits=('apple','cherry','banana','kiwi','guava')
#print from 0th inder to 4th index

#start:stop:step
print(fruits[0:5])      #runs upto 5(not 5)

#jump every 2nd element
print(fruits[0:5:2])

#jump every 3rd element
print(fruits[0:5:3])

#print all elements reverse
print(fruits[::-1])

#jump every 2nd element from the end
print(fruits[::-2]) 

