# printing entries
a=[20,40,60,10,30,90,100]         # from left - 0-20 ; 1-40
                        # from right - 1-1- ; 2-60
print(len(a))

print(a[-1])

print(a[-3])

print(a[1])

print(a[0:3])       # prints less than 3 ie 0,1,2
print(a[2:5])       # slices entries 0,1
print(a[-6:-1])

print(a[:])

print(a+[6,7])

print(a*2)